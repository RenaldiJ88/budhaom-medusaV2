type PaymentButtonProps = {
  cart: HttpTypes.StoreCart
  "data-testid": string
}

const PaymentButton: React.FC<PaymentButtonProps> = ({
  cart,
  "data-testid": dataTestId,
}) => {
  // 1. OBTENER LA SESI√ìN
  const paymentSession = cart.payment_collection?.payment_sessions?.[0]
  const providerId = paymentSession?.provider_id || "desconocido"

  // üîç LOG DE DEPURACI√ìN (Mira esto en la consola F12 del navegador)
  useEffect(() => {
    console.log("üïµÔ∏è [DEBUG PADRE] Provider ID detectado:", providerId)
    console.log("üïµÔ∏è [DEBUG PADRE] Datos de la sesi√≥n:", paymentSession)
  }, [providerId, paymentSession])

  // L√≥gica de validaci√≥n
  const notReady =
    !cart ||
    !cart.shipping_address ||
    !cart.billing_address ||
    !cart.email ||
    (cart.shipping_methods?.length ?? 0) < 1

  // 2. EL SWITCH (Modificado para ser m√°s flexible)
  switch (true) {
    case isStripe(providerId):
      return (
        <StripePaymentButton
          notReady={notReady}
          cart={cart}
          data-testid={dataTestId}
        />
      )
    case isManual(providerId):
      return (
        <ManualTestPaymentButton notReady={notReady} data-testid={dataTestId} />
      )
    case isPaypal(providerId):
      return (
        <PayPalPaymentButton
          notReady={notReady}
          cart={cart}
          data-testid={dataTestId}
        />
      )
    
    // üî• CAMBIO CLAVE: Usamos .includes() para atrapar cualquier variante de nombre
    case providerId.includes("mercadopago"): 
      console.log("‚úÖ [DEBUG PADRE] ¬°Seleccionando bot√≥n de Mercado Pago!")
      return (
        <MercadoPagoPaymentButton
          notReady={notReady} // Pasamos el estado real
          cart={cart}
          session={paymentSession}
        />
      )

    default:
      console.log("‚ö†Ô∏è [DEBUG PADRE] Ning√∫n caso coincidi√≥ para:", providerId)
      return <Button disabled>M√©todo de pago no reconocido: {providerId}</Button>
  }
}

